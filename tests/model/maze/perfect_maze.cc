INSTANTIATE_TEST_SUITE_P(maze_data, PerfectMaze,
                         testing::Range(0, PerfectMaze::count_));

TEST_P(PerfectMaze, CreatePerfectMaze) {
  int num_test = this->GetParam();
  PerfectMazeData& test_data = PerfectMaze::test_data_[num_test];

  mc::Maze maze;
  std::vector<int> true_vert = test_data.vertical_wals_;
  std::vector<int> true_horiz = test_data.horizontal_wals_;

  mc::Maze::ReturnCode code = maze.CreatePerfectMaze(
      test_data.rows_, test_data.columns_, test_data.rand_);
  ASSERT_EQ(code, mc::Maze::ReturnCode::kCodeOk);

  mtlc::Matrix<mc::Maze::value_type> vert = maze.GetVerticalMatrix();
  mtlc::Matrix<mc::Maze::value_type> horiz = maze.GetHorizontalMatrix();

  ASSERT_EQ(test_data.rows_, maze.GetRows());
  ASSERT_EQ(test_data.columns_, maze.GetColumns());

  for (int i = 0, k = 0; i < test_data.rows_; ++i) {
    for (int j = 0; j < test_data.columns_; ++j, ++k) {
      EXPECT_EQ(vert(i, j), true_vert[k]);
    }
  }

  for (int i = 0, k = 0; i < test_data.rows_; ++i) {
    for (int j = 0; j < test_data.columns_; ++j, ++k) {
      EXPECT_EQ(horiz(i, j), true_horiz[k]);
    }
  }
}

TEST(PerfectMaze, ErrorSize1) {
  mc::Maze maze;
  mc::Maze::ReturnCode code = maze.CreatePerfectMaze(4, 54);
  ASSERT_EQ(code, mc::Maze::ReturnCode::kCodeInvalidSize);
}

TEST(PerfectMaze, ErrorSize2) {
  mc::Maze maze;
  mc::Maze::ReturnCode code = maze.CreatePerfectMaze(54, 4);
  ASSERT_EQ(code, mc::Maze::ReturnCode::kCodeInvalidSize);
}
